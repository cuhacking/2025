function toCamelCase(sentence: string) {
  return sentence
    .replace(/[^a-z0-9 ]/gi, '') // Remove non-alphanumeric characters
    .split(' ')
    .map((word, index) =>
      index === 0
        ? word.toLowerCase()
        : word.charAt(0).toUpperCase() + word.slice(1).toLowerCase(),
    )
    .join('')
}

